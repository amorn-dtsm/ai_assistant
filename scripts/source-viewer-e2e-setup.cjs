/**
 * Source Viewer E2E Setup Script
 * 
 * Processes test fixtures through collector converters, creates workspace,
 * inserts workspace_documents and workspace_chats rows for E2E testing.
 * 
 * Run from project root: node scripts/source-viewer-e2e-setup.js
 */
const path = require("path");
const fs = require("fs");
const { v4: uuidv4 } = require(path.join(__dirname, "../collector/node_modules/uuid"));

// ── Paths ──
const ROOT = path.resolve(__dirname, "..");
const COLLECTOR = path.join(ROOT, "collector");
const SERVER = path.join(ROOT, "server");
const FIXTURES = path.join(ROOT, "test-fixtures", "source-viewer");
const STORAGE = path.join(SERVER, "storage");
const DOCS_DIR = path.join(STORAGE, "documents");
const CUSTOM_DOCS = path.join(DOCS_DIR, "custom-documents");
const ORIGINALS = path.join(DOCS_DIR, "originals");
const OCR_DIR = path.join(DOCS_DIR, "ocr");
const DB_PATH = path.join(STORAGE, "anythingllm.db");

// ── Ensure directories ──
[CUSTOM_DOCS, ORIGINALS, OCR_DIR].forEach(d => fs.mkdirSync(d, { recursive: true }));

// ── Load Prisma ──
const { PrismaClient } = require(path.join(SERVER, "node_modules/@prisma/client"));
const prisma = new PrismaClient({ datasources: { db: { url: `file:${DB_PATH}` } } });

// ── Collector converters ──
const asPdf = require(path.join(COLLECTOR, "processSingleFile/convert/asPDF/index.js"));
const asDocx = require(path.join(COLLECTOR, "processSingleFile/convert/asDocx.js"));
const asTxt = require(path.join(COLLECTOR, "processSingleFile/convert/asTxt.js"));
const asImage = require(path.join(COLLECTOR, "processSingleFile/convert/asImage.js"));

const WORKSPACE_NAME = "qa-viewer";
const LATIN_ANCHOR = "The quick brown fox anchors here.";
const THAI_ANCHOR = "การทดสอบระบบค้นหาข้อความภาษาไทย";

/**
 * Process a fixture file through the appropriate converter.
 * Returns the document data object (with location field).
 */
async function processFixture(filename, converter, extraOpts = {}) {
  const fullFilePath = path.join(FIXTURES, filename);
  console.log(`\n=== Processing: ${filename} ===`);
  
  const result = await converter({
    fullFilePath,
    filename,
    options: { absolutePath: true, ...extraOpts },
    metadata: {},
  });

  if (!result.success) {
    console.error(`FAILED: ${filename} - ${result.reason}`);
    return null;
  }

  const doc = result.documents[0];
  console.log(`OK: sourceId=${doc.sourceId}, contentType=${doc.contentType}, hasSourceViewer=${doc.hasSourceViewer}`);
  return doc;
}

/**
 * Create a manual document JSON + original + optional sidecar.
 * Used as fallback when converter fails (e.g., OCR timeout).
 */
function createManualDoc({ filename, sourceId, contentType, pageContent, extension, sidecar }) {
  const fullFilePath = path.join(FIXTURES, filename);
  
  // Copy original
  const destPath = path.join(ORIGINALS, `${sourceId}${extension}`);
  fs.copyFileSync(fullFilePath, destPath);

  // Write sidecar if provided
  if (sidecar) {
    fs.writeFileSync(path.join(OCR_DIR, `${sourceId}.json`), JSON.stringify(sidecar), "utf-8");
  }

  // Write document JSON
  const data = {
    id: sourceId,
    url: `file://${fullFilePath}`,
    title: filename,
    docAuthor: "Test",
    description: "Test fixture",
    docSource: "test fixture",
    chunkSource: "",
    published: new Date().toLocaleString(),
    wordCount: pageContent.split(" ").length,
    pageContent,
    token_count_estimate: Math.ceil(pageContent.length / 4),
    sourceId,
    contentType,
    hasSourceViewer: true,
  };

  const jsonFilename = `${filename.replace(/[^a-zA-Z0-9]/g, "-")}-${sourceId}`;
  const jsonPath = path.join(CUSTOM_DOCS, `${jsonFilename}.json`);
  fs.writeFileSync(jsonPath, JSON.stringify(data, null, 4), "utf-8");

  return {
    ...data,
    location: `custom-documents/${jsonFilename}.json`,
  };
}

/**
 * Find the document JSON file generated by a converter.
 */
function findDocJson(sourceId) {
  const files = fs.readdirSync(CUSTOM_DOCS);
  const match = files.find(f => f.includes(sourceId) && f.endsWith(".json"));
  if (!match) return null;
  return JSON.parse(fs.readFileSync(path.join(CUSTOM_DOCS, match), "utf-8"));
}

/**
 * Build a fake chat response JSON with sources.
 */
function buildChatResponse(text, sources) {
  return JSON.stringify({
    text,
    type: "textResponse",
    sources: sources.map(s => ({
      title: s.title || s.filename || "unknown",
      chunk: s.chunkText,
      sourceId: s.sourceId || undefined,
      hasSourceViewer: s.hasSourceViewer || false,
      contentType: s.contentType || undefined,
      chunkSource: s.chunkSource || "",
      score: 0.95,
    })),
    metrics: { prompt_tokens: 50, completion_tokens: 100, duration: 0.5 },
    attachments: [],
    outputs: [],
    clarifyingQuestions: [],
  });
}

async function main() {
  console.log("=== Source Viewer E2E Setup ===\n");

  // ── 1. Process fixtures ──
  const docs = {};

  // PDF (born-digital, Latin)
  docs.latinPdf = await processFixture("digital-latin.pdf", asPdf);

  // PDF (born-digital, Thai)
  docs.thaiPdf = await processFixture("digital-thai.pdf", asPdf);

  // DOCX
  docs.docx = await processFixture("simple.docx", asDocx);

  // Markdown (processed as txt)
  docs.md = await processFixture("sample.md", asTxt);

  // Text with BOM
  docs.txt = await processFixture("sample-bom.txt", asTxt);

  // Scanned PDF - try converter, fall back to manual
  try {
    docs.scannedPdf = await Promise.race([
      processFixture("scanned.pdf", asPdf),
      new Promise((_, reject) => setTimeout(() => reject(new Error("OCR timeout")), 120000)),
    ]);
  } catch (e) {
    console.log(`Scanned PDF OCR timed out/failed, creating manual data...`);
  }
  if (!docs.scannedPdf || !docs.scannedPdf.hasSourceViewer) {
    const sid = uuidv4();
    docs.scannedPdf = createManualDoc({
      filename: "scanned.pdf",
      sourceId: sid,
      contentType: "scanned-pdf",
      pageContent: "The quick brown fox anchors here. This is a scanned document with OCR text.",
      extension: ".pdf",
      sidecar: {
        version: 1, sourceId: sid, contentType: "scanned-pdf",
        pages: [{
          pageNumber: 1, width: 612, height: 792,
          lines: [
            { text: "The quick brown fox anchors here.", bbox: [0.1, 0.15, 0.9, 0.2] },
            { text: "This is a scanned document with OCR text.", bbox: [0.1, 0.25, 0.9, 0.3] },
          ],
        }],
      },
    });
  }

  // Latin OCR image - try converter, fall back to manual
  try {
    docs.latinImg = await Promise.race([
      processFixture("latin-ocr.jpg", asImage),
      new Promise((_, reject) => setTimeout(() => reject(new Error("OCR timeout")), 120000)),
    ]);
  } catch (e) {
    console.log(`Latin image OCR timed out/failed, creating manual data...`);
  }
  if (!docs.latinImg || !docs.latinImg.hasSourceViewer) {
    const sid = uuidv4();
    docs.latinImg = createManualDoc({
      filename: "latin-ocr.jpg",
      sourceId: sid,
      contentType: "image",
      pageContent: "The quick brown fox anchors here.\nLine two of the image.\nLine three of the image.",
      extension: ".jpg",
      sidecar: {
        version: 1, sourceId: sid, contentType: "image",
        pages: [{
          pageNumber: 1, width: 800, height: 200,
          lines: [
            { text: "The quick brown fox anchors here.", bbox: [0.05, 0.1, 0.95, 0.35] },
            { text: "Line two of the image.", bbox: [0.05, 0.4, 0.7, 0.65] },
            { text: "Line three of the image.", bbox: [0.05, 0.7, 0.75, 0.95] },
          ],
        }],
      },
    });
  }

  // Thai OCR image - try converter, fall back to manual
  try {
    docs.thaiImg = await Promise.race([
      processFixture("thai-ocr.png", asImage, { ocr: { langList: "tha,eng" } }),
      new Promise((_, reject) => setTimeout(() => reject(new Error("OCR timeout")), 180000)),
    ]);
  } catch (e) {
    console.log(`Thai image OCR timed out/failed, creating manual data...`);
  }
  if (!docs.thaiImg || !docs.thaiImg.hasSourceViewer) {
    const sid = uuidv4();
    docs.thaiImg = createManualDoc({
      filename: "thai-ocr.png",
      sourceId: sid,
      contentType: "image",
      pageContent: "การทดสอบระบบค้นหาข้อความภาษาไทย\nบรรทัดที่สองของภาพ",
      extension: ".png",
      sidecar: {
        version: 1, sourceId: sid, contentType: "image",
        pages: [{
          pageNumber: 1, width: 800, height: 200,
          lines: [
            { text: "การทดสอบระบบค้นหาข้อความภาษาไทย", bbox: [0.05, 0.1, 0.95, 0.45] },
            { text: "บรรทัดที่สองของภาพ", bbox: [0.05, 0.55, 0.65, 0.9] },
          ],
        }],
      },
    });
  }

  // ── 2. Create legacy doc (no sourceId) ──
  const legacyId = uuidv4();
  const legacyData = {
    id: legacyId,
    url: "file://legacy-doc.txt",
    title: "legacy-doc.txt",
    docAuthor: "Legacy",
    description: "Legacy document without source viewer",
    docSource: "legacy upload",
    chunkSource: "",
    published: new Date().toLocaleString(),
    wordCount: 10,
    pageContent: "This is a legacy document without source viewer support.",
    token_count_estimate: 12,
    // NO sourceId, NO hasSourceViewer, NO contentType
  };
  const legacyJsonPath = path.join(CUSTOM_DOCS, `legacy-doc-${legacyId}.json`);
  fs.writeFileSync(legacyJsonPath, JSON.stringify(legacyData, null, 4), "utf-8");
  docs.legacy = { ...legacyData, location: `custom-documents/legacy-doc-${legacyId}.json` };

  // ── 3. Verify all docs processed ──
  const docList = Object.entries(docs);
  console.log(`\n=== Processed ${docList.length} documents ===`);
  for (const [key, doc] of docList) {
    if (!doc) {
      console.error(`MISSING: ${key}`);
      continue;
    }
    console.log(`  ${key}: sourceId=${doc.sourceId || "NONE"}, type=${doc.contentType || "NONE"}, viewer=${doc.hasSourceViewer || false}`);
  }

  // ── 4. Create workspace + DB rows ──
  console.log("\n=== Setting up database ===");

  // Delete existing qa-viewer workspace if it exists
  const existing = await prisma.workspaces.findFirst({ where: { slug: "qa-viewer" } });
  if (existing) {
    await prisma.workspace_chats.deleteMany({ where: { workspaceId: existing.id } });
    await prisma.workspace_documents.deleteMany({ where: { workspaceId: existing.id } });
    await prisma.workspaces.delete({ where: { id: existing.id } });
    console.log("Deleted existing qa-viewer workspace");
  }

  // Create workspace
  const workspace = await prisma.workspaces.create({
    data: {
      name: WORKSPACE_NAME,
      slug: "qa-viewer",
      chatMode: "chat",
    },
  });
  console.log(`Created workspace: id=${workspace.id}, slug=${workspace.slug}`);

  // Insert workspace_documents for each doc
  const docRows = [];
  for (const [key, doc] of docList) {
    if (!doc) continue;
    const docId = uuidv4();
    const docpath = doc.location || `custom-documents/${key}-${doc.id || doc.sourceId}.json`;
    const row = await prisma.workspace_documents.create({
      data: {
        docId,
        filename: doc.title || `${key}.json`,
        docpath,
        workspaceId: workspace.id,
        metadata: JSON.stringify({
          title: doc.title,
          sourceId: doc.sourceId,
          hasSourceViewer: doc.hasSourceViewer,
          contentType: doc.contentType,
        }),
      },
    });
    docRows.push({ key, row, doc });
    console.log(`  workspace_documents: ${key} → docId=${docId}`);
  }

  // ── 5. Create fake chat rows ──
  console.log("\n=== Creating fake chat rows ===");

  // Helper to get chunk text for a doc
  function getChunkText(doc, anchor) {
    const pc = doc.pageContent || "";
    const idx = pc.indexOf(anchor);
    if (idx >= 0) {
      return pc.substring(Math.max(0, idx - 20), Math.min(pc.length, idx + anchor.length + 50));
    }
    return pc.substring(0, Math.min(200, pc.length));
  }

  // Chat 1: Latin PDF citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the PDF say?",
      response: buildChatResponse(
        "The PDF contains the text about a quick brown fox.",
        [{ ...docs.latinPdf, chunkText: getChunkText(docs.latinPdf, LATIN_ANCHOR), filename: "digital-latin.pdf" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 1: Latin PDF");

  // Chat 2: Thai PDF citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the Thai PDF say?",
      response: buildChatResponse(
        "The Thai PDF contains Thai text about testing.",
        [{ ...docs.thaiPdf, chunkText: getChunkText(docs.thaiPdf, THAI_ANCHOR), filename: "digital-thai.pdf" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 2: Thai PDF");

  // Chat 3: DOCX citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the DOCX say?",
      response: buildChatResponse(
        "The DOCX contains text about a quick brown fox.",
        [{ ...docs.docx, chunkText: getChunkText(docs.docx, LATIN_ANCHOR), filename: "simple.docx" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 3: DOCX");

  // Chat 4: Markdown citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the markdown say?",
      response: buildChatResponse(
        "The markdown file has a heading and anchor text.",
        [{ ...docs.md, chunkText: getChunkText(docs.md, LATIN_ANCHOR), filename: "sample.md" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 4: Markdown");

  // Chat 5: Text citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the text file say?",
      response: buildChatResponse(
        "The text file contains the anchor sentence.",
        [{ ...docs.txt, chunkText: getChunkText(docs.txt, LATIN_ANCHOR), filename: "sample-bom.txt" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 5: Text");

  // Chat 6: Scanned PDF citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the scanned PDF say?",
      response: buildChatResponse(
        "The scanned PDF contains OCR-extracted text.",
        [{ ...docs.scannedPdf, chunkText: getChunkText(docs.scannedPdf, LATIN_ANCHOR), filename: "scanned.pdf" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 6: Scanned PDF");

  // Chat 7: Latin image citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the image say?",
      response: buildChatResponse(
        "The image contains OCR-extracted English text.",
        [{ ...docs.latinImg, chunkText: getChunkText(docs.latinImg, LATIN_ANCHOR), filename: "latin-ocr.jpg" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 7: Latin image");

  // Chat 8: Thai image citation
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the Thai image say?",
      response: buildChatResponse(
        "The Thai image contains OCR-extracted Thai text.",
        [{ ...docs.thaiImg, chunkText: getChunkText(docs.thaiImg, THAI_ANCHOR), filename: "thai-ocr.png" }]
      ),
      include: true,
    },
  });
  console.log("  Chat 8: Thai image");

  // Chat 9: Legacy doc citation (no sourceId)
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "What does the legacy doc say?",
      response: buildChatResponse(
        "The legacy document has some text.",
        [{
          title: "legacy-doc.txt",
          chunkText: "This is a legacy document without source viewer support.",
          // NO sourceId, NO hasSourceViewer, NO contentType
        }]
      ),
      include: true,
    },
  });
  console.log("  Chat 9: Legacy doc");

  // Chat 10: Multi-source citation (for cross-format test)
  await prisma.workspace_chats.create({
    data: {
      workspaceId: workspace.id,
      prompt: "Compare all documents",
      response: buildChatResponse(
        "Here is a comparison of all documents.",
        [
          { ...docs.latinPdf, chunkText: getChunkText(docs.latinPdf, LATIN_ANCHOR), filename: "digital-latin.pdf" },
          { ...docs.docx, chunkText: getChunkText(docs.docx, LATIN_ANCHOR), filename: "simple.docx" },
          { ...docs.latinImg, chunkText: getChunkText(docs.latinImg, LATIN_ANCHOR), filename: "latin-ocr.jpg" },
          { ...docs.md, chunkText: getChunkText(docs.md, LATIN_ANCHOR), filename: "sample.md" },
        ]
      ),
      include: true,
    },
  });
  console.log("  Chat 10: Multi-source (cross-format)");

  // ── 6. Write manifest for E2E tests ──
  const manifest = {
    workspaceId: workspace.id,
    workspaceSlug: workspace.slug,
    documents: {},
  };
  for (const [key, doc] of docList) {
    if (!doc) continue;
    manifest.documents[key] = {
      sourceId: doc.sourceId,
      contentType: doc.contentType,
      hasSourceViewer: doc.hasSourceViewer,
      title: doc.title,
      location: doc.location,
    };
  }
  const manifestPath = path.join(ROOT, ".sisyphus", "evidence", "task-15", "e2e-manifest.json");
  fs.mkdirSync(path.dirname(manifestPath), { recursive: true });
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2), "utf-8");
  console.log(`\nManifest written to: ${manifestPath}`);

  await prisma.$disconnect();
  console.log("\n=== Setup complete ===");
}

main().catch(err => {
  console.error("Setup failed:", err);
  process.exit(1);
});
